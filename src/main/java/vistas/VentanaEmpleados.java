    private void cargarDatosEnTabla() {
        System.out.println("üîç Iniciando carga de datos de empleados...");

        // Definir columnas
        String[] columnas = {
            "ID",
            "C√©dula",
            "Nombres",
            "Apellidos",
            "Direcci√≥n",
            "Tel√©fono Convencional",
            "Tel√©fono Celular",
            "Correo Electr√≥nico"
        };

        // Crear modelo de tabla
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
                null, columnas
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        try {
            EmpleadoDAO empleadoDAO = new EmpleadoDAO();
            System.out.println("‚úÖ EmpleadoDAO creado correctamente");

            List<Empleado> empleados = empleadoDAO.listarTodos();
            System.out.println("üìä N√∫mero de empleados recuperados: " + empleados.size());

            for (Empleado emp : empleados) {
                System.out.println("üìÑ Empleado: ID=" + emp.getEmpId() + ", C√©dula=" + emp.getEmpCedula());
                Object[] fila = {
                    emp.getEmpId(),
                    emp.getEmpCedula(),
                    emp.getEmpNombre(),
                    emp.getEmpApellido(),
                    emp.getEmpDireccion(),
                    emp.getEmpTelefonoConvencional(),
                    emp.getEmpTelefonoCelular(),
                    emp.getEmpCorreoElectronico()
                };
                modelo.addRow(fila);
            }

        } catch (SQLException e) {
            System.out.println("‚ùå ERROR SQL al cargar empleados: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Error al cargar los empleados:\n" + e.getMessage(),
                    "Error de base de datos",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            System.out.println("üí• ERROR GENERAL al cargar empleados: " + e.getMessage());
            e.printStackTrace();
        }

        // Asignar el modelo a la tabla
        jTableEmpleados.setModel(modelo);
        System.out.println("‚úÖ Tabla actualizada con el modelo");
    }

